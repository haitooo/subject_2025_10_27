#include <stdio.h>

/*
 * このプログラムは、入力された N を直径とみなして、
 * 「#」で塗りつぶされた円盤っぽい形を出力。
 *
 * 考え方：
 *  - 文字をマス目と考える。
 *  - そのマスの「中心」が円の半径の内側にあるなら '#'
 *    外側なら ' ' (空白) を出す。
 *
 * 注意：
 * 問題文では「半角文字1つ = 高さ1, 横幅0.5」として扱う、とある。
 * なので、縦方向は N 行、横方向は 2*N 列にすると、
 * 見かけ上の縦横スケールが揃って丸く見えやすい。
 */

int main(void)
{
    int N;

    /* N が範囲外なら再入力させる、とあるのでループでチェック */
    while (1) {
        printf("Input N:");
        if (scanf("%d", &N) != 1) {
            /* 整数が読めなかったら終了してしまう */
            return 1;
        }

        /*
         * 1 <= N <= 30
         */
        if (N >= 1 && N <= 30) {
            break; /* 妥当な値なら抜ける */
        }
    }

    /*
     * 円の中心(cx, cy)と半径rを決める。
     * N=直径 なので 半径 r = N/2。
     * 画面上では行が0～N-1、列が0～(2N-1)になる。
     * 「(0,0) のマスの中心座標がどこか」を決めて、
     * それぞれのマスの中心から中心(cx,cy)までの距離を測る。
     */

    double r = N / 2.0; /* 半径 */
    double cx = r;      /* 中心の x 座標 */
    double cy = r;      /* 中心の y 座標 */

    int height = N;     /* 行数（縦方向） */
    int width  = 2 * N; /* 列数（横方向）。2倍にして横が0.5倍の扱いになる */

    /*
     * (x, y) のマスの「中心座標」をどう決めるか？
     *
     *  縦方向:
     *    y 行目のマスの中心は、y + 0.5 あたりとする。
     *    (マス1個ぶんの高さは1なので、真ん中は +0.5)
     *
     *  横方向:
     *    1文字の「見かけの横幅」は0.5とする。
     *    x 列目の左端が x*0.5 だとすると、
     *    中心は x*0.5 + 0.25 あたり。
     *
     * そこから中心(cx,cy)までの距離を計算して、
     * 半径r以内なら '#', それ以外は ' ' とする。
     */

    for (int y = 0; y < height; y++) {
        for (int x = 0; x < width; x++) {

            /* いま描画しているマスの中心(見かけ上の座標) */
            double px = x * 0.5 + 0.25;
            double py = y * 1.0 + 0.5;

            /* 中心との距離を2乗の形で計算する (平方根を使わないため) */
            double dx = px - cx;
            double dy = py - cy;
            double dist2 = dx*dx + dy*dy; /* 距離の2乗 */

            /*
             * dist2 <= r*r なら円の中(または境界)とみなして '#'
             * それ以外は空白。
             *
             * 「境界は自由でよい」と問題文にあるので、
             * <= にするか < にするかは好きにしてOK。
             * レポートに「半径以内(<=)を # とした」と書けばよい。
             */
            if (dist2 <= r * r) {
                putchar('#');
            } else {
                putchar(' ');
            }
        }
        putchar('\n');
    }

    return 0;
}
